def _is_latin_line(line: str) -> bool:
    stripped = line.strip()
    if not line.startswith("  ") or not stripped:
        return False
    return stripped[0].isascii() and stripped[0].isalpha()


class PitchPdf(FPDF):
    def header(self):
        if self.page_no() == 1:
            return
        self.set_font("NotoMal", "", 8)
        self.set_text_color(*MUTED)
        self.cell(0, 6, TITLE, align="L")
        self.cell(0, 6, "മലയാളം ജ്യോതിഷ കൂട്ടുകാരി", align="R",
                  new_x="LMARGIN", new_y="NEXT")
        self.ln(2)

    def footer(self):
        if self.page_no() == 1:
            return
        self.set_y(-13)
        self.set_font("NotoMal", "", 8)
        self.set_text_color(*MUTED)
        self.cell(0, 8, f"{self.page_no()}", align="C")

    def chapter(self, title: str, blocks: list[tuple[str, str]]):
        self.add_page()
        self.set_font("NotoMal", "", 10)
        self.set_text_color(*ACCENT)
        self.cell(0, 6, "സ്റ്റാർട്ടപ്പ് പിച്ച്", new_x="LMARGIN", new_y="NEXT")
        self.set_font("NotoMal", "", 18)
        self.set_text_color(*INDIGO)
        self.multi_cell(0, 9.5, title, new_x="LMARGIN", new_y="NEXT")
        self.set_draw_color(*GOLD)
        self.set_line_width(1.0)
        self.line(self.l_margin, self.get_y() + 1.5, self.l_margin + 28, self.get_y() + 1.5)
        self.ln(6)
        for heading, body in blocks:
            if heading:
                self.set_font("NotoMal", "", 12.5)
                self.set_text_color(*ACCENT)
                self.multi_cell(0, 7, heading, new_x="LMARGIN", new_y="NEXT")
                self.ln(0.5)
            self.set_font("NotoMal", "", 11)
            self.set_text_color(*INK)
            for raw in body.split("\n"):
                line = raw.rstrip()
                if _is_latin_line(line):
                    text = line.strip()
                    self.set_font("NotoMal", "", 10)
                    self.set_fill_color(*BOX_BG)
                    self.set_text_color(*BOX_TEXT)
                    x = self.get_x()
                    self.set_x(x + 4)
                    self.multi_cell(0, 6.2, text, fill=True, new_x="LMARGIN", new_y="NEXT")
                    self.set_x(x)
                    self.set_font("NotoMal", "", 11)
                    self.set_text_color(*INK)
                elif line.startswith("- "):
                    x = self.get_x()
                    self.set_x(x + 4)
                    self.set_text_color(*ACCENT)
                    self.cell(4, 6.6, chr(8226))
                    self.set_text_color(*INK)
                    self.multi_cell(0, 6.6, line[2:], new_x="LMARGIN", new_y="NEXT")
                    self.set_x(x)
                elif line == "":
                    self.ln(2.5)
                else:
                    self.multi_cell(0, 6.6, line, new_x="LMARGIN", new_y="NEXT")
            self.ln(4)


